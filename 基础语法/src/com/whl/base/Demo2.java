public class Demo2 {
    public static void main(String[] args) {
        //八大基本数据类型
        //整数
        Integer  ab = 2;
        int a = 10;
        byte b = 127;
        short c = 11;
        long d = 238494028L;//long类型要在后面加L

        //小数：浮点数
        float e = 11.2F;//float类型要在后面加F
        double f = 3.1415926;

        //字符
        char name = 'a';
        String sex = "female";//String不是关键字，是类

        //布尔值：是非
        boolean flag1 = true;
        boolean flag2 = false;

    }
}
